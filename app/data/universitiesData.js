export const getCollegesForUniversity = (universityKey) => {
  if (!universityKey || !universitiesData[universityKey]) {
    return [];
  }
  return universitiesData[universityKey].colleges;
};

export const getAllColleges = () => {
  const allColleges = new Set();
  Object.values(universitiesData).forEach(uni => {
    uni.colleges.forEach(college => allColleges.add(college));
  });
  return Array.from(allColleges);
};

